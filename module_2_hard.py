def gen_pass(n):
    # Инициализация пустой строки для хранения пар
    passwd = ""
    # Цикл по всем числам от 1 до n-1
    for i in range(1, n):
        # Вложенный цикл от i+1 до n
        for j in range(i + 1, n + 1):
            # Проверка условий делимости для формирования пар
            if n % (i + j) == 0 or (i + j) % n == 0:
                passwd += f"{i}{j}"  # Добавление пары к строке
    return passwd  # Возврат сгенерированной строки


# Запрос числа у пользователя
user_input = int(input("Введите число: "))
# Проверка корректности введённого числа (в диапазоне от 3 до 20 включительно)
if user_input in range(3, 21):
    result = gen_pass(user_input)  # Генерация пар на основе введённого числа
    print(result)  # Вывод сгенерированной строки
else:
    print("Введено неверное число")  # Сообщение об ошибке, если число вне диапазона
