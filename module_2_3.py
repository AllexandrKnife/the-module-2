# Исходные данные
my_list = [42, 69, 322, 13, 0, 99, -5, 9, 8, 7, -6, 5]

# Начальный индекс
i = 0

# Цикл для перебора списка
while i < len(my_list):
    # Если встречаем отрицательное число, прерываем цикл
    if my_list[i] < 0:
        break

    # Если число больше 0, выводим его на консоль
    if my_list[i] > 0:
        print(my_list[i])

    # Переход к следующему элементу
    i += 1
